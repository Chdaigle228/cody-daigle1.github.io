function range(start, end, num){ 

  let arr = [];
  if(start === end){
  return arr;
  } if(num !== undefined){
   if(num < 0){
    return arr;
   }
     for (var i = start; i <= end; i += num)
        arr.push(i);
    } else {
      for (var i = start; i <= end; i ++)
        arr.push(i);
    } return arr;
  }

